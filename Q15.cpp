#include <iostream>
using namespace std;

int main(){
    int n;
    cout << "Enter a positive integer: ";
    cin >> n;

    int lagrgest = 0;
    while (n--)
    {
        int temp;
        cout << "Enter number: ";
        cin >> temp;
        if(temp > lagrgest || lagrgest == 0){
            lagrgest = temp;
        }
    }
    cout << "Largest number is: " << lagrgest << endl;
    return 0;
    
}